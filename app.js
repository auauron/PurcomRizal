function saveScore() {
  localStorage.setItem("quizScore", JSON.stringify(state.score));
}

function updateScore() {
  const { correct, total } = state.score;
  const accuracy = total ? Math.round((correct / total) * 100) : 0;
  els.accuracyText.textContent = `${accuracy}%`;
  els.scoreText.textContent = `${correct} correct of ${total}`;
}

